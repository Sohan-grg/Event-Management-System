    private void startLoading() {

        Thread t = new Thread(() -> {
            try {
                for (int i = 0; i <= 100; i++) {
                    jProgressBar1.setValue(i);
                    Thread.sleep(10); // loading speed
                }

                // After loading finishes â†’ open Login Page
                new LoginPage().setVisible(true);
                this.dispose(); // close loading page

            } catch (InterruptedException e) {
                e.printStackTrace();
            }
        });

        t.start();
    }



